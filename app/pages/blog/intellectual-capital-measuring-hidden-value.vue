    <h3>تحلیل عملیاتی مدل DIC: ارزش در معرض خطر (Value at Risk)</h3>
    <p>
      در یک سناریوی مدیریتی، اگر یک شرکت نرم‌افزاری بخواهد ارزش دانش تیم متخصص ۳۰ نفره‌ی خود را
      بسنجد، هزینه‌ی جایگزینی (شامل جذب، آموزش و افت بهره‌وری در دوره‌ی یادگیری) برای هر متخصص ۶۱۰
      میلیون تومان برآورد می‌شود. این بدان معناست که ۱۸.۳ میلیارد تومان از دارایی‌های دانشی سازمان
      در قالب «هزینه‌ی جایگزینی» قرار دارد؛ رقمی که در حسابداری سنتی دیده نمی‌شود اما در صورت خروج
      تیم، سازمان را با بحرانی جدی مواجه می‌کند.
    </p>

    <h2>۶. شکاف استراتژیک: نگاه مدیریتی در برابر حسابداری</h2>
    <p>
      تضاد بنیادین میان دیدگاه حسابداری و مدیریتی در نحوه‌ی برخورد با «سرقفلی» (Goodwill) نهفته است.
      بر اساس منطق موجود در مدل‌های ارزش‌گذاری:
    </p>
    <p>
      <strong>Market Value = IC Value = Goodwill + IA Value</strong>
    </p>
    <p>
      در حالی که استانداردهای حسابداری (مانند IAS 38) تنها دارایی‌های نامشهود (IA) قابل شناسایی و
      متمایز از سرقفلی را به رسمیت می‌شناسند، مدیران ارشد مسئول خلق «سرقفلی» یا همان سینرژی حاصل از
      ترکیب دارایی‌ها هستند. حسابداری سنتی با نادیده گرفتن این هم‌افزایی، بخشی از ارزش‌آفرینی
      مدیریتی را «نامرئی» باقی می‌گذارد (Gioacasi, 2014).
    </p>


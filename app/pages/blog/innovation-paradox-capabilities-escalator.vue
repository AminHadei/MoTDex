      مشارکت مالی (Co-financing) بخش خصوصی. ابزارهایی مانند «گرنت‌های تطبیقی مبتنی بر مناقصه» که در
      آن شرکت‌ها برای کمترین یارانه مورد نیاز رقابت می‌کنند، ریسک رانت‌خواری را به شدت کاهش می‌دهند.
    </p>

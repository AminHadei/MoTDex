      </li>
      <li>
        <strong>مدل PATH (Supply-push):</strong>
        که در واکسن مننژیت به کار رفت و با هزینه بسیار کمتر (۷۰ میلیون دلار در مقابل ۱.۵ میلیارد دلار در AMC) به نتیجه رسید.

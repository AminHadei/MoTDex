    <h2>۱. مقدمه: پایان عصر «انتخاب برندگان» و طلوع سیاست‌گذاری آزمایشی</h2>
    <p>
      دهه‌هاست که سیاست‌گذاران در اقتصادهای در حال توسعه به دنبال فرمولی جادویی برای جهش اقتصادی بوده‌اند. با این حال، تاریخ سیاست‌گذاری صنعتی سنتی مملو از شکست‌هایی است که ناشی از رویکردهای «بالا به پایین» و تلاش دولت‌ها برای «انتخاب برندگان» (Picking Winners) بوده است. این مدل‌های قدیمی اغلب در تله عدم تقارن اطلاعاتی گرفتار شده و به جای نوآوری، منجر به رانت‌جویی و تقویت منافع گروه‌های خاص شده‌اند. دولت‌ها به دلیل دیدگاه جزئی به اقتصاد و نداشتن نمای پانوراما (Panoramic View)، نمی‌توانستند به درستی تشخیص دهند که کدام بخش یا تکنولوژی واقعاً پتانسیل رشد جهانی را دارد.

    <h3>تفاوت «رانت» سنتی با «شبه-رانت» (Quasi-rents) در این مدل چیست؟</h3>
    <p>
      رانت‌های سنتی بر پایه انحصار و روابط سیاسی توزیع می‌شوند و دائمی هستند. اما «شبه-رانت‌ها» در سیاست‌گذاری آزمایشی، مشروط به عملکرد، نوآوری و دستیابی به نقاط عطف (Milestones) فنی هستند. این رانت‌ها ابزاری برای انگیزه دادن به خلاقیت هستند و در صورت عدم تحقق اهداف، بلافاصله قطع می‌شوند.
    </p>

    <h3>چرا استراتژی «طراحی محلی، اجرا در سطح جهانی» برای شرکت‌های چندملیتی جذاب است؟</h3>
    <p>
      چون آن‌ها متوجه شده‌اند که رشد بازارهای اشباع‌شده غرب متوقف شده است. با طراحی محصولات در هند یا چین (مانند دستگاه سونوگرافی ارزان GE)، آن‌ها نه تنها بازارهای نوظهور را به دست می‌آورند، بلکه استانداردهای جدیدی برای بهره‌وری خلق می‌کنند که در بازارهای جهانی به آن‌ها برتری می‌دهد.
    </p>

    <h3>نقش بخش خصوصی در سیاست‌گذاری آزمایشی چیست؟</h3>
    <p>
      بخش خصوصی به عنوان «سنسور» عمل می‌کند. دولت به تنهایی نمی‌داند موانع کجاست؛ این کارآفرینان هستند که در میدان عمل با موانع روبرو می‌شوند. دولت در این مدل، شریک استراتژیک بخش خصوصی برای رفع این گلوگاه‌ها و ایجاد «تعهد پیشرفته خرید» برای محصولات نوآورانه است.
    </p>


      اینجاست که نرخ پذیرش (Grant Rate) در دفتری مانند استرالیا به ۹۹٪ می‌رسد (که نشان‌دهنده یک
      سیستم سهل‌گیرانه‌تر است)، در حالی که چین با رد حدود نیم میلیون پرونده در سال ۲۰۲۴،
      سخت‌گیرانه‌ترین نرخ پذیرش را در میان دفاتر بزرگ داشته است.
    </p>
    <p>
      <strong>جعبه بینش:</strong>
      ظهور «پتنت یکپارچه اروپایی» (Unitary Patent) در سال ۲۰۲۳، بازی را در اروپا تغییر داده است. این
      سیستم به مخترعان اجازه می‌دهد با یک درخواست، در ۱۸ کشور عضو اتحادیه اروپا حفاظت دریافت کنند؛
      ابزاری که به شدت هزینه‌های اداری را برای شرکت‌های بین‌المللی کاهش داده است.
